def find_sum(num1, num2):
    """
    This function takes two numbers, num1 and num2,
    adds them, and returns the sum.
    """
    # Adding num1 and num2
    num_sum = num1 + num2

    # Returning the sum
    return num_sum

# Calling the function with arguments 111 and 222
my_sum = find_sum(111, 222)

# Printing the result
print(my_sum)

# The output is 333 because 111 + 222 equals 333, and that result is stored in the variable my_sum and printed. 